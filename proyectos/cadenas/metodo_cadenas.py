# Uso de metodos para las cadenas
# Todo lo que se hace con los metodos crea otra cadena

texto_utilizar = 'Mi amorcito bello'
print(f'Tu eres {texto_utilizar}')

# Mayuscula
mayuscula = texto_utilizar.upper()
print(f'Tu eres {mayuscula}')

#Minuscula
print(f'Tu eres {texto_utilizar.lower()}')

#Eliminar espacios
texto_utilizar = ' Mi amorcito bello '
print(f'Tu eres {texto_utilizar.strip()}') # ELimina espacio al inicio y final


