# Ciclos
# Los ciclos son intrucciones que se
# repiten hasta que se cumplan

# Existe varios tipos pero
# los principales son: (WHILE Y FOR)

# WHILE
bucle_close = 1
while bucle_close <= 2:
    print(bucle_close)
    bucle_close += 1
    print(bucle_close)

print()

#FOR
bucle_close = 'HOLA'
for letras in bucle_close:
    print(letras, end = '')