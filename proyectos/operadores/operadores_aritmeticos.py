# Los operadores aritmeticos
# Se utlizan para realiza operaciones matematicas

# Valores de prueba
a = 3
b = 3

# Suma
print(f'Suma: {a + b}')

# Resta
print(f'Resta: {a - b}')

# Multiplicacion
print(f'Multiplicacion: {a * b}')

# Division
print(f'Divicion: {a / b:2f}')

# Division Entera
print(f'Divicion: {a // b}')

# Modulo (Lo que queda de la division)
print(f'Residuo de la Divicion: {a % b}')

# Potencia
print(f'Potencia: {a ** b}')
