# Asignacion Compuesto
# Son los mismo que los aritmeticos lo unico es
# que funcionan con el operador de asignacion
# osea funcionan cuando se les asigna un valor

# Valores de ejemplo
a, b = 10, 2

# Ejemplo

# Suma
a += b
print(f'a += b: {a}')

# Resta
a = 10
a -= b
print(f'a -= b: {a}')

# Multiplicacion
a = 10
a *= b
print(f'a *= b: {a}')

# Division
a = 10
a /= b
print(f'a /= b: {a}')
