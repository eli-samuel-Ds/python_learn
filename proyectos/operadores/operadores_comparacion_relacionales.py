# Operadores de Comparacion (Relacionales)
print('== Operadores de Comparacion (Relacionales) ==')
# Su nombre lo indica compara valores
# y devuelbe un valor boolean

# Valores de prueba
a, b = 1, 2

# Ejemplos
# Comparacion ==
print(f'a == b: {a == b}')
# Diferencia !=
print(f'a != b: {a != b}')
# Mayor que
print(f'a > b: {a > b}')
# Mayor o igual que
print(f'a >= b: {a >= b}')
# Menor que
print(f'a < b: {a < b}')
# Menor o igual que
print(f'a <= b: {a <= b}')
