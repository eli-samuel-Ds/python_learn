# Operadores logicos
# Estos operadores se utilizan para
# hacer logica con valores boolean

# Valores de prueba
valor_uno, valor_dos = False, False

# Operador AND (Los dos deben ser iguales)
print(f'Los valores ({valor_uno}:{valor_dos}) con el operador (AND) devuelve: {valor_uno and valor_dos}')

# Operador OR (Uno de los dos)
print(f'Los valores ({valor_uno}:{valor_dos}) con el operador (OR) devuelve: {valor_uno or valor_dos}')

# Operador NOT (Invierte los valores)
print(f'Los valores ({valor_uno}) con el operador (NOT) devuelve: {not valor_uno}')
