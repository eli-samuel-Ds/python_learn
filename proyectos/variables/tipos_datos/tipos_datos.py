# Manejo de datos

# Enteros
edad = 20
print('Edad:', edad)
print('Su tipo es:', type(edad), '\n')

# Flotante
altura = 1.90
print('Altura:', altura)
print('Su tipo es:', type(altura), '\n')

# Cadena
apellido = 'Juana'
print('Apellido:', apellido)
print('Su tipo es:', type(apellido), '\n')

# Boolean
es_alto = False
print('Es alto ?', es_alto)
print('Su tipo es:', type(es_alto), '\n')

# None se ustiliza cuando no se sabe el valor de la variable
numero_casa = None
print('Numero de Casa:', numero_casa)
print('Su tipo es:', type(numero_casa))
