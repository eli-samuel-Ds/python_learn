# Trabajando Variables en Python

# Declarar y Inicializar
edad_mia = 18
altura_mia = 1.56
pais_mio = 'RD'

# Usando variables
print('Valores sin modificar')
print('Edad:', edad_mia)
print('Altura:', altura_mia)
print('Pais', pais_mio)

# Modificar variable
edad_mia = 30
altura_mia = '1.90'
pais_mio = 'Canada'

# Usando variables
print('Valores modificados')
print('Edad:', edad_mia)
print('Altura:', altura_mia)
print('Pais', pais_mio)

# Las variables son dinamicas en Python
edad_mia = 'treinta'
print(edad_mia)
